// Helper function to get image for a menu item
// First checks for local images in /images/menu/, then falls back to mapped URLs
export const getMenuItemImage = (itemId: string): string => {
  // Try local image first (if you add images to public/images/menu/)
  const localImage = `/images/menu/${itemId}.jpg`;
  
  // Return mapped image from Pexels/Unsplash
  return menuItemImages[itemId] || 'https://images.pexels.com/photos/1640777/pexels-photo-1640777.jpeg?auto=compress&cs=tinysrgb&w=400&h=400&fit=crop';
};

